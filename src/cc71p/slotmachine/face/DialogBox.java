package cc71p.slotmachine.face;
/**
 * Clase que representa el Dialog Box presente en la Slot Machine
 * 
 * @author daniel
 *
 */
public class DialogBox {
	/**
	 * Imprime texto en la Dialog Box
	 * 
	 * @param message Mensaje a imprimir
	 */
	public void print(String message){
		System.out.println("************** DIALOG BOX ****************");
		System.out.print(message);
		System.out.println("******************************************");
	}
}
